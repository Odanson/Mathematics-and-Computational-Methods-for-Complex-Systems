function classification_loss(w, b)
	p = sigmoid.(X_dset1[:, 2] + w .* X_dset1[:, 1] .+ b)
	l_pos = dot(Y_dset1, p)
	l_neg = dot(1 .- Y_dset1, 1 .- p)
	
	bce = (l_pos + l_neg)
	#reg = 1e-2 * (w1^2 + w2^2)
	return bce# + reg
end

