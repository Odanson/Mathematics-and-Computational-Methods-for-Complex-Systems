## Gradient Descent in 1D
Imagine you're hiking down a mountain and want to reach the bottom as quickly as possible. You start at a random spot on the mountain and look around to see which direction is the steepest way downhill. You take a step in that direction and repeat the process: look around, find the steepest way downhill, and take a step. You keep doing this until you reach a flat spot or the bottom of the mountain.

In the context of optimization, we use a similar idea called gradient descent. Instead of a mountain, we have a mathematical function that we want to minimize. At the start, we pick a random point on the function. We look at the slope of the function at that point, which tells us the direction of steepest descent. We take a step in the opposite direction of the slope and land at a new point. We repeat this process: look at the slope, take a step, and keep going until we reach the lowest point or a stopping condition.

This picture should help (in this example, the parameters they are optimizing for are the weights for a machine learning model):

![Alt text](https://editor.analyticsvidhya.com/uploads/83237Slide12.PNG)


In what follows, you might like to use this finite-difference operator from a previous week.  This is handy for calculating gradients, but I encourage you to try and do most of them yourself.  You shouldn't need anything too crazy this week.  It's mostly just polynomials and trig functions.
"""

# ╔═╡ 8595a1f1-611b-4a82-8828-295cef78e3dd
function return_fd_operator(Δx::Number) 
	function D(f::Function)
		function dfdx(x)
			y = (f(x + Δx) - f(x))/(Δx)
			return y
		end
		return dfdx
	end
	return D
end

# ╔═╡ 5932078f-fc95-4967-8363-5dc00061e525
md"""
Here is our workhorse, the gradient descent algorithm.  Make sure you understand what each line is doing.
"""

# ╔═╡ 51f647cc-fbfa-41f4-9551-044fbd2a78e0
function grad_descent(
		func::Function, grad_func::Function, n_steps::Integer, step_size::Number, x_init::Number
)
    x = zeros(n_steps, length(x_init))
	x[1] = x_init

	fx = zeros(n_steps)
	fx[1] = func(x_init)
    for i = 2:n_steps; 
		x[i] = x[i-1] - step_size*grad_func(x[i-1])
		fx[i] = func(x[i])
	end
    (x, fx)
end

# ╔═╡ dcad091a-7d5a-40c6-bd22-4fbaa578015c
md"""
Some comments on the arguments:

- ```func``` is the objective we want to optimize.
- ```grad_func``` is the first-derivative of ```func``` wrt the variable we want to optimize.
- ```n_steps``` tells the function how many iterations of GD to perform.
- ```step_size``` the size of the update we make to our variable once we have a direction.
- ```x_init``` is our initial guess for where the optimum is.

Note that this algorithm MINIMIZES the function.  If you want to maximize a function, you would add the gradient, not subtract it (adding the gradient gives you gradient ascent).

Let's get a feel for what these parameters mean.


