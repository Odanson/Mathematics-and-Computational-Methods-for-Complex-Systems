# ╔═╡ 0195ceb9-0482-4238-a894-f690846f891b
md"""
In the examples we looked at, we saw that, with the right step-size, GD reliably finds the optima we are looking for.  While we only looked at simple power functions, it turns out GD is guaranteed to work for any CONVEX function.  Here's the first defintion we need:

!!! info "Definition"
	A set $S$ is a convex set if, for any two points $x_1$ and $x_2$ in $S$, and for any value $t$ between 0 and 1, the following holds:

	$$tx_1 + (1-t)x_2 \in S$$

In simpler terms, a set is convex if, for any two points within the set, the line segment connecting those two points is entirely contained within the set.  In our 1D problems all of our sets will be convex (unless the interval we search over is disconnected).  In 2D that may not always be the case.

Here's the second definition we need:

!!! info "Definition"
	A function $f(x)$ defined on a convex set $S$ is said to be convex if, for any two points $x_1$ and $x_2$ in $S$, and for any value $t$ between 0 and 1, the following inequality holds:

	$$f(tx_1 + (1-t)x_2) \leq tf(x_1) + (1-t)f(x_2)$$

Intuitively, a convex function is one where if you take any two points on the graph of the function and draw a straight line between them, the function's graph will be either below or touching that line. It doesn't curve upwards away from the line.


!!! info "Question"
	Why do you think, intuitively, convexity allows GD to find the optimum?

